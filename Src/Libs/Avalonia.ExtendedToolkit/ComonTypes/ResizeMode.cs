namespace Avalonia.ExtendedToolkit
{
    /// <summary>
    /// possible resizemodes
    /// </summary>
    public enum ResizeMode
    {
        /// <summary>
        /// do not resize
        /// </summary>
        NoResize,

        /// <summary>
        /// can minimize
        /// </summary>
        CanMinimize,

        /// <summary>
        /// can resize
        /// </summary>
        CanResize,

        /// <summary>
        /// can resize with grip
        /// </summary>
        CanResizeWithGrip
    }
}
